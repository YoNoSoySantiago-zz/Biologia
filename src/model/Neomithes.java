package model;

public class Neornithes
{
	private double longitudCola;
	private double densidadOsea;
	private String rangoMetabolico;
	public String BAJO="bajo";
	public String MEDIO="medio";
	public String ALTO="alto";
	
	public Neornithes(double longitudCola, double densidadOsea, String rangoMetabolico)
	{
		this.longitudCola=longitudCola;
		this.densidadOsea=densidadOsea;
		this.rangoMetabolico=rangoMetabolico;
	}
}